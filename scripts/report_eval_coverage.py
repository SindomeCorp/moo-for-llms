#!/usr/bin/env python3
"""Report eval coverage and optionally export runnable prompt rows."""

from __future__ import annotations

import argparse
import json
from collections import Counter
from pathlib import Path
from typing import Any, Iterable


ROOT = Path(__file__).resolve().parents[1]
EVALS_DIR = ROOT / "evals"


def main() -> int:
    parser = argparse.ArgumentParser(description="Report eval coverage by kind, dialect, topic, and file")
    parser.add_argument("--kind", action="append", help="Only include eval rows with this kind; may be repeated")
    parser.add_argument("--dialect", action="append", help="Only include eval rows with this dialect; may be repeated")
    parser.add_argument("--topic", action="append", help="Only include eval rows inferred from this filename stem; may be repeated")
    parser.add_argument("--output", type=Path, help="Optional markdown report path")
    parser.add_argument("--prompts-output", type=Path, help="Optional JSONL prompt export path")
    args = parser.parse_args()

    rows = [row for row in load_eval_rows() if matches(row, args)]
    duplicate_ids = duplicate_values(row["id"] for row in rows)
    markdown = render_markdown(rows, duplicate_ids)

    if args.output:
        output = absolute(args.output)
        output.parent.mkdir(parents=True, exist_ok=True)
        output.write_text(markdown, encoding="utf-8")
    else:
        print(markdown, end="")

    if args.prompts_output:
        output = absolute(args.prompts_output)
        output.parent.mkdir(parents=True, exist_ok=True)
        write_jsonl(output, prompt_rows(rows))

    if duplicate_ids:
        print(f"duplicate eval id(s): {', '.join(duplicate_ids)}")
        return 1
    return 0


def load_eval_rows() -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for path in sorted(EVALS_DIR.rglob("*.jsonl")):
        for line_number, line in enumerate(path.read_text(encoding="utf-8").splitlines(), start=1):
            if not line.strip():
                continue
            row = json.loads(line)
            rel_path = path.relative_to(ROOT).as_posix()
            rows.append(
                {
                    "source_file": rel_path,
                    "topic": path.stem,
                    "line": line_number,
                    **row,
                }
            )
    return rows


def matches(row: dict[str, Any], args: argparse.Namespace) -> bool:
    if args.kind and row.get("kind") not in set(args.kind):
        return False
    if args.dialect and row.get("dialect") not in set(args.dialect):
        return False
    if args.topic and row.get("topic") not in set(args.topic):
        return False
    return True


def duplicate_values(values: Iterable[str]) -> list[str]:
    counts = Counter(values)
    return sorted(value for value, count in counts.items() if count > 1)


def render_markdown(rows: list[dict[str, Any]], duplicate_ids: list[str]) -> str:
    lines = [
        "# Eval Coverage Report",
        "",
        "Generated by `python3 scripts/report_eval_coverage.py`.",
        "",
        "## Summary",
        "",
        f"- eval rows: {len(rows)}",
        f"- duplicate ids: {len(duplicate_ids)}",
        "",
        "## By Kind",
        "",
    ]
    append_counts(lines, counts_by(rows, "kind"))
    lines.extend(["", "## By Dialect", ""])
    append_counts(lines, counts_by(rows, "dialect"))
    lines.extend(["", "## By Topic", ""])
    append_counts(lines, counts_by(rows, "topic"))
    lines.extend(["", "## By File", ""])
    append_counts(lines, counts_by(rows, "source_file"))
    if duplicate_ids:
        lines.extend(["", "## Duplicate IDs", ""])
        for value in duplicate_ids:
            lines.append(f"- `{value}`")
    return "\n".join(lines).rstrip() + "\n"


def counts_by(rows: list[dict[str, Any]], key: str) -> dict[str, int]:
    counts: dict[str, int] = {}
    for row in rows:
        value = str(row.get(key, "unspecified"))
        counts[value] = counts.get(value, 0) + 1
    return dict(sorted(counts.items()))


def append_counts(lines: list[str], counts: dict[str, int]) -> None:
    if not counts:
        lines.append("None.")
        return
    for key, value in counts.items():
        lines.append(f"- `{key}`: {value}")


def prompt_rows(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    output: list[dict[str, Any]] = []
    for row in rows:
        exported = {
            "id": row["id"],
            "source_file": row["source_file"],
            "topic": row["topic"],
            "kind": row["kind"],
            "dialect": row["dialect"],
            "prompt": row["prompt"],
        }
        for key in ("input", "expected", "expected_properties"):
            if key in row:
                exported[key] = row[key]
        output.append(exported)
    return output


def write_jsonl(path: Path, rows: list[dict[str, Any]]) -> None:
    path.write_text("".join(json.dumps(row, separators=(",", ":")) + "\n" for row in rows), encoding="utf-8")


def absolute(path: Path) -> Path:
    return path if path.is_absolute() else ROOT / path


if __name__ == "__main__":
    raise SystemExit(main())
